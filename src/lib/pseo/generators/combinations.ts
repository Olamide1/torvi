// ─── Generator function ────────────────────────────────────────────────────────

function generateCombinationPage(
  topic: TopicBase,
  role: (typeof ROLES)[number]
): PSEOPage {
  const titleTopic = topic.topic
    .replace(" templates and prompts", " template")
    .replace(" templates", " template")
    .replace(" guides and templates", " guide")
    .replace(" guides", " guide");

  const slug = `${topic.slug}-for-${role.key}`;
  const keyword = `${titleTopic} for ${role.name}`;
  const h1 = `${titleTopic.charAt(0).toUpperCase() + titleTopic.slice(1)} for ${role.name}`;
  const title = `${h1} — Free Download | Torvi`;
  const metaDescription = `Free ${titleTopic} designed for ${role.name}. Structured, fast to use, and built around how ${role.short} actually work.`;

  return {
    slug,
    title,
    h1,
    metaDescription,
    keyword,
    cluster: topic.cluster,
    pageType: topic.pageType,
    priorityScore: topic.priorityScore,
    body: {
      intro: role.intro(titleTopic),
      whatYouGet: topic.baseWhatYouGet,
      whoItsFor: role.whoItsFor,
      ctaNote: role.ctaNote,
    },
  };
}

// Generate all combinations — skip if the base slug is already in the main pages list
// (role-specific variants will still be generated since their slugs are unique)
const SKIP_BASE_ROLES: RoleKey[] = []; // can add base slugs to exclude specific roles

export const combinationPages: PSEOPage[] = TOPICS.flatMap((topic) =>
  ROLES.filter((role) => !SKIP_BASE_ROLES.includes(role.key)).map((role) =>
    generateCombinationPage(topic, role)
  )
);

// Export for use in pages.ts
export const totalCombinations = combinationPages.length;
